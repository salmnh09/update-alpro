public class menghitunglingkaran {
    public static void main(String[] args){
        double jarijari=7.5;
        double phi=3.14;
        //menghitung luas lingkaran
        double luas = phi*jarijari*jarijari;
        System.out.println("Luas lingkaran dengan jari-jari : " + jarijari + "adalah" + luas);
        //keliling lingkaran
        double keliling = 2 *phi*jarijari;
        System.out.println("Keliling lingkaran dengn jari-jari : " + jarijari + "adalah " + keliling );
    }
    
}
